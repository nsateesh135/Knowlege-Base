# Sets are unordered collection of unique elements 
# one use case would be to find out all unique elements in a list 


cities = ['Bangalore','Chennai','Delhi','Mumbai','Bangalore','Chennai']
x = set(cities)
print(x) #Output: {'Bangalore','Chennai','Delhi','Mumbai'}

# Methods 
cities.add('Punjab') # Output :{'Bangalore','Chennai','Delhi','Mumbai','Punjab'}

cities.remove('Delhi')

